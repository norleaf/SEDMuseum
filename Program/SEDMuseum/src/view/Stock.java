    public void updateStock() {
        ShopControl sc = mf.getShopControl();
        ArrayList<ProductGroup> productGroupList = sc.getProductGroupList();
        for (ProductGroup productGroup : productGroupList) {
            ArrayList<Product> tempList = sc.getProductList(productGroup);
            for (Product product : tempList) {
                if (productGroup.getNr() != 1 && productGroup.getNr() != 4) {
                    jTextArea1.append(product.getName() + ": " + product.getAmount() + "\n");
                }
            }
        }
    }

